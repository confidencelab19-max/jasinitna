function tableRows(rows) {
  return rows.map((row) => `<tr>${row.map((cell) => `<td>${cell}</td>`).join('')}</tr>`).join('\n');
}

function listItems(items) {
  return items.map((item) => `<li>${item}</li>`).join('\n');
}

