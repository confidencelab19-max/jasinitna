function visualBlock(categoryDir, slug, title) {
  const profile = visualProfiles[`${categoryDir}/${slug}`] ?? fallbackVisuals[categoryDir] ?? fallbackVisuals.start;
  const rows = profile.rows ?? fallbackVisuals[categoryDir]?.rows ?? fallbackVisuals.start.rows;
  return `<div className="guide-visual guide-visual--${categoryDir}">
  <div className="guide-visual__copy">
    <strong>${title} 화면에서 이렇게 확인해요</strong>
    <p>${profile.summary}</p>
    <ol>
      ${profile.steps.map((step) => `<li>${step}</li>`).join('\n      ')}
    </ol>
  </div>
  <div className="visual-screen" aria-label="${title} 확인 화면">
    <div className="visual-screen__bar"></div>
    <div className="visual-screen__body">
      ${rows
        .map(
          (row, index) => `<div className="visual-screen__row">
        <i></i>
        <span>${row[0]}</span>
        <em>${row[1] ?? (index === 0 ? '먼저' : '확인')}</em>
      </div>`,
        )
        .join('\n      ')}
      <div className="visual-screen__table">
        <span></span>
        <span></span>
        <span></span>
      </div>
    </div>
  </div>
</div>`;
}

