function clean(value) {
  return String(value).replace(/\r\n/g, '\n').replace(/[ \t]+$/gm, '').replace(/\n{3,}/g, '\n\n').trim();
}

function descriptionFrom(body, fallback) {
  const firstLine = clean(body)
    .split('\n')
    .map((line) => line.trim())
    .find((line) => line && !line.startsWith('#') && !line.startsWith('-') && !line.startsWith('|'));

  if (!firstLine) return fallback;
  if (firstLine.length <= 130) return firstLine;

  const sentenceEnd = firstLine.lastIndexOf('요.', 130);
  if (sentenceEnd >= 30) return firstLine.slice(0, sentenceEnd + 2);

  const lastSpace = firstLine.lastIndexOf(' ', 130);
  return `${firstLine.slice(0, lastSpace > 40 ? lastSpace : 130)}...`;
}

